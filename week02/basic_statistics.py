import numpy as np

# ==========================================================
# 2.10 기초 통계함수
# 배열의 합, 평균, 최소값, 최대값을 구하는 기본 함수들
# 영상처리에서도 픽셀 단위로 자주 사용됨
# ==========================================================

# 배열 생성 (3행 4열)
a = np.arange(12).reshape(3, 4)
print("a =\n", a)

# ------------------------------
# 합계 (sum)
# ------------------------------
print("\nnp.sum(a) =", np.sum(a))          # 전체 합
print("np.sum(a, 0) =", np.sum(a, 0))      # 열(column) 방향 합
print("np.sum(a, 1) =", np.sum(a, 1))      # 행(row) 방향 합

# ------------------------------
# 평균 (mean)
# ------------------------------
print("\nnp.mean(a) =", np.mean(a))        # 전체 평균
print("np.mean(a, 0) =", np.mean(a, 0))    # 열별 평균
print("np.mean(a, 1) =", np.mean(a, 1))    # 행별 평균

# ------------------------------
# 최소값 (amin)
# ------------------------------
print("\nnp.amin(a) =", np.amin(a))        # 전체 최소값
print("np.amin(a, 0) =", np.amin(a, 0))    # 열별 최소값
print("np.amin(a, 1) =", np.amin(a, 1))    # 행별 최소값

# ------------------------------
# 최대값 (amax)
# ------------------------------
print("\nnp.amax(a) =", np.amax(a))        # 전체 최대값
print("np.amax(a, 0) =", np.amax(a, 0))    # 열별 최대값
print("np.amax(a, 1) =", np.amax(a, 1))    # 행별 최대값
